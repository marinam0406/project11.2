def upper_string(string):
    """
    Функция, возвращает строку большими буквами
    :param string: строка
    :return: строку с большими буквами
    """
    return string.upper()


def title_string(string):
    """
    Функция, возвращает слова с заглавной буквы
    :param string: строка
    :return: строку со словами с большой буквы
    """
    return string.title()